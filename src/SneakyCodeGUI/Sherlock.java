    public void setFrecuencias(String message){
        int size = message.length();
        Bumblebee bee = new Bumblebee();
        Key.setText(String.valueOf(bee.honeyPot3(message)));
        Map<String,Integer> frecuency = bee.getFrecuency();
        Map<String,Integer> sherlock = bee.getSherlock();
        int i = 0;
        Object [][] row = new Object[frecuency.size()][2];
        String [] modelHeader = {"Car치cter en texto","Frecuencia"};
        String [] clavesHeader = {"Crypt","Original","Clave"};
        Iterator<String> it = frecuency.keySet().iterator();
        while(it.hasNext()){
            String clave = it.next();
            row[i][0]=clave; row[i][1]=String.format("%.2f %s",((double)frecuency.get(clave)/(double)size)*100,"%");
            //model.addRow(row); 
            i++;
        }
        
        i = 0;
        Object [][] keys = new Object[sherlock.size()][3];
        it = sherlock.keySet().iterator();
        while(it.hasNext()){
            String clave = it.next();
            keys[i][0] = clave; keys[i][1]= "A"; keys[i][2]= bee.location("A", clave);
            //claves.addRow(keys);
            i++;
        }
        
        model = new DefaultTableModel(row, modelHeader){@Override
                                        public boolean isCellEditable(int row, int column) {
                                            return false;} };
        claves = new DefaultTableModel(keys,clavesHeader){@Override
                                        public boolean isCellEditable(int row, int column) {
                                            return false;} };
        TextFrecuency.setModel(model);
        KeyTable.setModel(claves);
    }
    
    
    
